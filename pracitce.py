
name = "jhoncarlnayao"
#length of the string
print(len(name))
#find method (finding the index of it)
print(name.find("H"))
#capitalize the name
print(name.capitalize())
#uppercase
print(name.upper())
#lowercase
print(name.lower())
#if this variable is digit (this will print true if the variable is in number)
print(name.isdigit())
#(contain)check if this is alphabetical characters(remove the space to make the result true even your variable is in alphabetical)
print(name.isalpha())
#count how many characters are in here
print(name.count("a"))
#replace characters
print(name.replace("a","o"))
#display string multiple times
print(name*100)
